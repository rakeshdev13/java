package AllProgram;

public class EvenorOdd {

	public static void main(String[] args) {
		// print Even or odd seperately using if 
		
		int num=10;
		if(num%2==0) {
			System.out.println("It is an Even number");
		}
		if(num%2==1) {
			System.out.println("It is an Odd number");
		}
	}

}
